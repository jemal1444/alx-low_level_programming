/**
 * _abs - return the absolute value of a variable.
 * @n: variable for input value.
 * Return: absolute value.
 */

int _abs(int n)
{
        if (n < 0)
        {
                n = n * -1;
                return (n);
        }
        else
        {
                return (n);
        }
}
